public class att7 {
    public static void main(String[] args) {
        int numero = 101; // começa em 101, pois queremos os números ENTRE 100 e 200

        System.out.println("Números entre 100 e 200 (ordem crescente):");

        while (numero < 200) {
            System.out.println(numero);
            numero++;
        }
    }
}
